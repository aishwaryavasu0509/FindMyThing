package com.example.demo.model;

public class Buisnesscentre{
	private int startYear;
	private String goal;
	private String partnership;
	private String outreach;
	private String servicesOrProducts;
	
	public Buisnesscentre()
	{
		
	}
	
	public int getStartYear() {
		return startYear;
	}
	public void setStartYear(int startYear) {
		this.startYear = startYear;
	}
	public String getGoal() {
		return goal;
	}
	public void setGoal(String goal) {
		this.goal = goal;
	}
	public String getPartnership() {
		return partnership;
	}
	public void setPartnership(String partnership) {
		this.partnership = partnership;
	}
	public String getOutreach() {
		return outreach;
	}
	public void setOutreach(String outreach) {
		this.outreach = outreach;
	}
	public String getServicesOrProducts() {
		return servicesOrProducts;
	}
	public void setServicesOrProducts(String servicesOrProducts) {
		this.servicesOrProducts = servicesOrProducts;
	}

	
}
